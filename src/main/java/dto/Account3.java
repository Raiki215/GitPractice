package dto;

public class Account3 {
	private String name;
	private int age;
	private String gender;
	private String mail;
	private String number;
	public Account3(String name, int age, String gender, String mail, String number) {
		super();
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.mail = mail;
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	
	
	
}
